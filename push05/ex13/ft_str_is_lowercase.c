int	ft_str_is_lowercase(char *str)
{
	int i;
	int a;

	a = 0;
	i = 0;
	if (str[i] == '\0')
		return (1);
	while (str[i] != '\0')
	{
		if (str[i] >= 'a' && str[i] <= 'z')
			a = 1;
		else
			return (0);
		i++;
	}
	return (a);
}
