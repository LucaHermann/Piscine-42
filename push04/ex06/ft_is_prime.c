int	ft_is_prime(int nb)
{
	int i;
	int pr;

	pr = 1;
	i = 2;
	if (nb < 2)
		return (0);
	while (i <= nb - 1)
	{
		if (nb % i == 0)
			pr = 0;
		i++;
	}
	return (pr);
}
